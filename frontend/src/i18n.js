const TAG_KEY_MAP = { All: "tag_all", GC: "tag_gc", Sprint: "tag_sprint", Classics: "tag_classics", Climber: "tag_climber", Domestique: "tag_domestique" };
const STAGE_TAG_KEY_MAP = { TTT: "stagetag_ttt", Hilly: "stagetag_hilly", Mountain: "stagetag_mountain", Flat: "stagetag_flat", ITT: "stagetag_itt" };

export function translate(lang, key, vars) {
  const dict = I18N[lang] || I18N.en;
  let s = dict[key] !== undefined ? dict[key] : (I18N.en[key] !== undefined ? I18N.en[key] : key);
  if (vars) Object.keys(vars).forEach((k) => { s = s.split(`{${k}}`).join(vars[k]); });
  return s;
}

export function ordinalFor(lang, n) {
  if (lang === "nl") return `${n}e`;
  const sfx = ["th", "st", "nd", "rd"];
  const v = n % 100;
  return n + (sfx[(v - 20) % 10] || sfx[v] || sfx[0]);
}

export function tagLabelFor(lang, tag) { return translate(lang, TAG_KEY_MAP[tag] || tag); }
export function stageTagLabelFor(lang, tag) { return translate(lang, STAGE_TAG_KEY_MAP[tag] || tag); }
export function jerseyLabelFor(lang, jersey) { return translate(lang, `jersey_${jersey}`); }

/** Vue composable: reactive translation helpers bound to the store's current language. */
export function useI18n() {
  const store = usePouleStore();
  const lang = computed(() => store.lang);
  return {
    lang,
    t: (key, vars) => translate(lang.value, key, vars),
    ordinal: (n) => ordinalFor(lang.value, n),
    tagLabel: (tag) => tagLabelFor(lang.value, tag),
    stageTagLabel: (tag) => stageTagLabelFor(lang.value, tag),
    jerseyLabel: (jersey) => jerseyLabelFor(lang.value, jersey)
  };
}
